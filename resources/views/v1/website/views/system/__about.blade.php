                  <div class="elementor-text-editor elementor-clearfix"><p>FNB Educational, Inc. stands for growth, dynamism, and strategic thinking ahead of time. FNB started in 1978 with a vision to help bring out the best in the young Filipino through access to affordable, comprehensive, relevant, and genuinely effective instructional materials. The first set of elementary school textbooks in some core subjects printed in 1978 mirrored the idealism of its founder and hardworking staff of five. The challenge then, as it is today, was to ensure the quality in the content, design, and material of the textbooks being developed and printed. Marketing as well as distribution was handled by another outfit. The first few years were indeed challenging, inherent of start-up and growth.<strong style="font-size: 15px;"> </strong></p><p>In the early eighties, FNB offered a broader selection of textbooks and workbooks. It also started conducting seminars and workshops for educators, facilitated by competent FNB editors and authors. By 1989, the company had an expanded output of noteworthy titles and series.</p><p>Steadily, its management succeeded in assigning regional sales managers all over the country. FNB products made inroads nationwide with the organization of this coherent sales force. In 1990, FNB wound up the production of SEDP-based textbooks for the secondary level. From the core subjects – Language, Reading, Filipino, Wika and Pagbasa, Sibika / Araling Panlipunan, Science and Mathematics – for preschool, elementary, and secondary levels, FNB has branched out into other curricular areas, such as CLE, Values Education, PEHM, THE, CAT, MAPE, GMRC, HELE, Computer Education, and Writing. It has also addressed the growing need for supplementary and enrichment resources through the Filipino Supplementary Reader, Reading Skill Book, Language Workbook, NEAT and NSAT Practice Tests, Resource Book on Filipino heroes, and Science Laboratory Manual. </p><p>FNB responded to the first DECS Textbook Call of 1995, and most of the textbooks and teacher’s manuals it submitted for evaluation were officially approved for use in the public schools as per DECS ORDER No. 62, S. 1997. Likewise, as stated in DECS ORDER No. 8, S. 1999 and DECS ORDER No. 35, S. 2000, DECS-IMCS approved the majority of learning materials of FNB in the second DECS Textbook Call and Third DECS Textbook Call, respectively. </p><p>To keep pace with the fast-growing Information Technology (IT) industry and to help students unravel the maximum capabilities of their computers, the company provides the educational community with the most up-to-date and engaging Computer Education Series from preschool to high school. </p></div>
